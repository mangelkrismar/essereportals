function seleccionaTema(objeto){
    $(".d_emergenteTema").removeClass("d_emergenteTema_seleccionado")
    if (filtroTema == $(objeto).attr("numeroTema")) {
        filtroTema = "";
        $("#filtroTema td").text("Subtema")
        $("#emergenteDisco, #emergenteTema, #emergenteCategoria").hide()
        realizaConsulta();
        $("#filtroTema table").removeClass("d_menuTXT_selected");
    }else{
        $(objeto).addClass("d_emergenteTema_seleccionado")
        filtroTema = ""+$(objeto).attr("numeroTema");
        $("#filtroTema td").text($(objeto).text())
        $("#emergenteDisco, #emergenteTema, #emergenteCategoria").hide()
        realizaConsulta();
        $("#filtroTema table").addClass("d_menuTXT_selected");
    }
    //console.log(filtroTema)
    
}

